#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		len;
	char	*sdup;

	i = 0;
	len = ft_strlen(s);
	if (!(sdup = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	while (s[i])
	{
		sdup[i] = s[i];
		i++;
	}
	sdup[i] = '\0';
	return (sdup);
}
